package com.example.ggkgl.AssitClass;


/**
 * 辅助工具类，用于需要传key,value键值对类型的参数
 */
public class Change {
    private String value;
    private String key;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
